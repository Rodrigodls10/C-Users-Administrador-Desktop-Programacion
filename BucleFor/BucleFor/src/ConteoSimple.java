public class ConteoSimple {
    public static void main(String[] args) {
        /**
         * for (inicializacion; condicion; actualizacion)
         *     // bloque de codigo a ejecutar en cada repeticion o iteracion
         * }
         *   Inicializacion: se ejecuta antes de la primera iteracion. Decalara la variable de control
         *   condicion: se evaluaantes de cada iteracion. Si es verdadera, se ejecuta el bucle. Si es falsa se termina
         *   Actualizacion: se ejecuta despues de cada iteracion.
         */
        for (int i=0; i<=10; i++){
            System.out.println("Conteo: " + i);
        }


    }

}
